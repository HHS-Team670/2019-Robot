package frc.team670.robot.utils.math;

public class DrivePower {
	private double left;
	private double right;

	public DrivePower(double left, double right) {
		this.left = left;
		this.right = right;
	}

	public double getLeft() {
		return left;
	}

	public double getRight() {
		return right;
	}

}
